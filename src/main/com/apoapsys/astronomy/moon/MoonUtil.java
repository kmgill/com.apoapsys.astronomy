	protected static double degToDecimal(double degrees, double minutes,
			double seconds) {
		return degrees + (minutes / 60) + (seconds / 60 / 60);
	}

	protected static double minToDecimal(double minutes, double seconds) {
		return degToDecimal(0, minutes, seconds);
	}

	protected static double secToDecimal(double seconds) {
		return degToDecimal(0, 0, seconds);
	}

	protected static double sin(double v) {
		return Math.sin(v * Math.PI / 180);
	}

	protected static double cos(double v) {
		return Math.cos(v * Math.PI / 180);
	}

	protected static double tan(double v) {
		return Math.tan(v * Math.PI / 180);
	}

	protected static double cramp(double v, double within) {
		return v - within * Math.floor(v / within);
	}

	protected static double deg(double v) {
		return cramp(v, 360);
	}

	protected static double asin(double v) {
		return Math.asin(v) * 180 / Math.PI;
	}

	protected static double acos(double v) {
		return Math.acos(v) * 180 / Math.PI;
	}

	protected static double atan2(double y, double x) {
		return Math.atan2(y, x) * 180 / Math.PI;
	}

	protected static double atan(double v) {
		return asin(v / Math.sqrt(pow(v, 2) + 1));
	}

	protected static double sqrt(double v) {
		return Math.sqrt(v);
	}

	protected static double pow(double v, double e) {
		return Math.pow(v, e);
	}

