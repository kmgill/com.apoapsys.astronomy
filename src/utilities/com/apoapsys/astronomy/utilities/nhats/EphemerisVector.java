package com.apoapsys.astronomy.utilities.nhats;


public class EphemerisVector {
	private double x;
	private double y;
	private double z;
	private double vx;
	private double vy;
	private double vz;
	private double lt;
	private double range;
	private double rangeRate;
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getZ() {
		return z;
	}
	public void setZ(double z) {
		this.z = z;
	}
	public double getVx() {
		return vx;
	}
	public void setVx(double vx) {
		this.vx = vx;
	}
	public double getVy() {
		return vy;
	}
	public void setVy(double vy) {
		this.vy = vy;
	}
	public double getVz() {
		return vz;
	}
	public void setVz(double vz) {
		this.vz = vz;
	}
	public double getLt() {
		return lt;
	}
	public void setLt(double lt) {
		this.lt = lt;
	}
	public double getRange() {
		return range;
	}
	public void setRange(double range) {
		this.range = range;
	}
	public double getRangeRate() {
		return rangeRate;
	}
	public void setRangeRate(double rangeRate) {
		this.rangeRate = rangeRate;
	}
	
	
}	
